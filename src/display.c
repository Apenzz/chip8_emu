bool is_valid_pixel(unsigned short pixel) {
	// pixel 0xXXYY	
	unsigned char x_pos = (0xff00 & pixel) >> 8;
	unsigned char y_pos = 0x00ff & pixel;
	bool is_valid;
	return is_valid = ((x_pos < DISPLAY_WIDTH) && (y_pos < DISPLAY_HEIGHT)) ? true : false;
}
